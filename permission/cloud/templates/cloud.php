<h1>ownCloud</h1>

Die ownCloud ist die zentrale Datenverwaltung der GRÜNEN JUGEND. Sie ist dazu da, Dateien von Gremien, Arbeitsgruppen oder ähnlichem zentral zu verwalten und zu speichern. Es dient ausserdem dem zentralen Wissensmanagement der GRÜNEN JUGEND und unterstützt dabei die ehrenamtliche Arbeit.<hr>

<h1>ownCloud-Nutzung</h1>

Du bist berechtigt, die ownCloud zu nutzen. Du kannst dich bereits einloggen.<br><br>

Durch deine Gruppen-Mitgliedschaften im IGELoffice werden deinem ownCloud-Zugang diverse Ordner freigegeben. Diese Ordner sind deine einzige M&ouml;glichkeit, Dateien anzuschauen, zu verwalten, zu bearbeiten, herunterzuladen oder hochzuladen. Bitte beachte dabei den Sinn und Zweck des jeweiligen Ordners, der sich anhand dessen Namens ableiten kann.<br><br>

<b>Beachte bitte:</b>: Die ownCloud des Bundesverbandes ist nicht dein persönlicher Cloud-Space. Du hast nicht die Möglichkeit, Daten in deinem persönlichen Space hochzuladen oder sie dorthin zu verschieben.<br><br>

